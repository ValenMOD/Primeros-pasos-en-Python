def suma(num1, num2):
    
    resultado = num1 + num2
    print(resultado)
    
suma(5, 4)


    
def resta(num1, num2):
    
    resultado = num1 - num2
    print(resultado)

resta(9, 2)



def multiplica(num1, num2):
    
    resultado = num1 * num2
    print(resultado)

multiplica(3, 2)



